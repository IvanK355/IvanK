package Task1v2;

public abstract class Caculator {

    public int makeMultiplication(int a, int b) {
        return a*b;
    }

    public int makeSum(int a, int b) {
        return a+b;
    }

    public int makeSubtraction(int a, int b) {
        return a-b;
    }

    public Double makeDivision(int a, int b) {
        return a*1.0/b;
    }
}
